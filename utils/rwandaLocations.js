  // Helper function to get all locations as flat array for search
  export const getAllLocations = () => {
    const locations = []
  
    Object.keys(rwandaLocations).forEach((province) => {
      Object.keys(rwandaLocations[province].districts).forEach((district) => {
        Object.keys(rwandaLocations[province].districts[district].sectors).forEach((sector) => {
          const sectorData = rwandaLocations[province].districts[district].sectors[sector]
  
          // Add cells
          sectorData.cells.forEach((cell) => {
            locations.push({
              type: "cell",
              province,
              district,
              sector,
              name: cell,
              fullAddress: `${cell}, ${sector}, ${district}, ${province}`,
            })
          })
  
          // Add villages
          sectorData.villages.forEach((village) => {
            locations.push({
              type: "village",
              province,
              district,
              sector,
              name: village,
              fullAddress: `${village}, ${sector}, ${district}, ${province}`,
            })
          })
  
          // Add streets
          sectorData.streets.forEach((street) => {
            locations.push({
              type: "street",
              province,
              district,
              sector,
              name: street,
              fullAddress: `${street}, ${sector}, ${district}, ${province}`,
            })
          })
        })
      })
    })
  
    return locations
  }
  
  // Helper function to search locations
  export const searchLocations = (query, limit = 10) => {
    if (!query || query.length < 2) return []
  
    const allLocations = getAllLocations()
    const searchTerm = query.toLowerCase()
  
    return allLocations
      .filter(
        (location) =>
          location.fullAddress.toLowerCase().includes(searchTerm) || location.name.toLowerCase().includes(searchTerm),
      )
      .slice(0, limit)
  }
  
